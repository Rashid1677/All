// Write a js program to create Simple Calculator using switch case.

var a = 6
var b = 5
switch ("*") {
    case "+":
        console.log(a+b);
        
        
        break;
        case "-":
        console.log(a-b);
        
        
        break;
        case "*":
        console.log(a*b);
        
        
        break;
        case "/":
        console.log(a/b);
        
        
        break;
        

    default:
        console.log("inter correct number");
}
